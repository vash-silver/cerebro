/// <summary>
/// Maps a power's <b>root</b> prototype-enum index to the basename (no extension, no
/// directory) of the in-game UI icon for that power.  Resolves to a file under
/// <c>MarvelHeroes.DpsMeter/Images/powers/&lt;basename&gt;.png</c>, embedded as a WPF
/// <c>Resource</c> via the .csproj glob.  Look up the actual <see cref="System.Windows.Media.ImageSource"/>
/// with <see cref="GetPackUri"/>.
///
/// <para><b>Generation:</b> built from <c>scripts/PrototypeEnumDumper</c>'s
/// <c>icon-map.txt</c> output (which reads each <c>PowerPrototype.IconPath</c> AssetId
/// and translates the asset's name "MarvelUIIcons.Power_Storm_Typhoon" to the basename
/// "power_storm_typhoon") cross-referenced against the actually-bundled set produced by
/// <c>scripts/IconExtractor</c> against the MH client's ICO__MarvelUIIcons*_SF.upk
/// Scaleform asset packs.  Entries with no bundled PNG (the icon exists in MHServerEmu's
/// PowerPrototype data but we don't ship it -- e.g. achievement icons, store assets) are
/// filtered out at generation time so callers can rely on a non-null lookup mapping to a
/// real, loadable resource.</para>
///
/// <para>Used by the Buff Tracker's auto-suggest path: when the user clicks "Track" on a
/// buff, we look up the buff's source <c>CreatorPowerPrototypeRef</c> (root enum) in this
/// table and auto-populate the icon for that watchlist entry.  The user can still
/// override via the "Set icon..." file picker -- the auto-suggest only fills empty
/// slots.</para>
/// </summary>
internal static class PowerIconByProto
{
    /// <summary>Returns the bundled-icon basename for <paramref name="rootPowerProtoEnumIndex"/>,
    /// or <c>null</c> when the power has no matching icon in the bundle (either it doesn't
    /// have an IconPath in PowerPrototype, or its IconPath references an icon we don't ship
    /// -- see the class doc for the filtering rules).  The returned string is the filename
    /// WITHOUT extension; use <see cref="GetPackUri"/> to build a loadable WPF URI.</summary>
    public static string? Get(uint rootPowerProtoEnumIndex)
        => s_basenameByProto.TryGetValue(rootPowerProtoEnumIndex, out var n) ? n : null;

    /// <summary>Convenience: returns a WPF pack URI string for the icon, or <c>null</c>
    /// when no icon is bundled for this power.  Format:
    /// <c>pack://application:,,,/Images/powers/&lt;basename&gt;.png</c>.</summary>
    public static string? GetPackUri(uint rootPowerProtoEnumIndex)
    {
        var bn = Get(rootPowerProtoEnumIndex);
        return bn == null ? null : $"pack://application:,,,/Images/powers/{bn}.png";
    }

